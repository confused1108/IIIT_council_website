        <b>Digital India: </b>
        The vision of this initiative of the GoI is to transform India into a digitally empowered society and knowledge economy. IIITs are playing a major role in this by training and developing skilled IT professionals. IIITs are also putting significant efforts in developing new apps and software to boost this initiative.
        <br><br>
        <b>Startup India: </b>
        The aim of this initiative of the GoI is to empower Startups to grow through innovation and design. IIITs are devoted towards achieving this goal through their Incubation Centers for promoting technology and innovation.
        <br><br>
        <b>Make in India: </b>
        The vision of this initiative of the GoI is to give momentum to manufacturing in India and make India a global manufacturing hub. IIITs are putting significant efforts through research and projects in design and manufacturing domain.
